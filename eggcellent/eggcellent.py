import numpy as np


def make_egg(a=-1.25, b=7):
    """
    Return x, y points that resemble an egg.

    Egg equation is:

        r = cos(2θ) + a * cos(θ) + b

    @param a: Number.
    @param b: Number.
    """
    theta = np.linspace(0, 2 * np.pi, 100)
    r = np.cos(2 * theta) + a * np.cos(theta) + b
    y = r * np.cos(theta)
    x = r * np.sin(theta)

    return np.array([x, y]).T.tolist()


